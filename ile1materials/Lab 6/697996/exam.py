def all_parties(db : {str:{str:[int]}}) -> {str}:
    result=set()
    for k,v in db.items():
        result.add(k)
        for z in v.keys():
            result.add(z)
    return result



def read_db(file : open) -> {str:{str:[int]}}:
    ndict={}
    for line in file:
        line=line.strip('\n')
        nline=line.split(':')
        l=[]
        for x in nline[2::]:
            l.append(int(x))
        if nline[0] in ndict.keys():
            if nline[1] in ndict[nline[0]]:
                ndict[nline[0]][nline[1]].extend(l)
            else:
                ndict[nline[0]].update({nline[1]:l})
        else:
            ndict.update({nline[0]:{nline[1]:l}})
    return ndict


