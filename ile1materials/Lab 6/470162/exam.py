def all_parties(db : {str:{str:[int]}}) -> {str}:
    # {'caller': {'receiver': [minutes, minutes ..]}}
    retSet = set();
    for key,val in db.items():
        retSet.add(key);
        for k in val.keys():
            retSet.add(k);
        
    return retSet



def read_db(file : open) -> {str:{str:[int]}}:
    
    retDict = defaultdict(dict);

    for line in file:
        words = line.rstrip().split(':')
        retDict[words[0]].setdefault(words[1], [])
        
        retDict[words[0]][words[1]] += [int(time) for time in words[2::]]
    
        #sets key
        
    file.close();
    return dict(retDict)



def talker_frequency (db : {str:{str:[int]}}) -> [(str,int)]:
    #returns  [(caller/reciever,#calls)] in decreasing order. If tow names have same number appear in increasing alphabetical
    personDict = defaultdict(int)
    
    for caller, innerDict in db.items():
        for callee, callTime in innerDict.items():
            num_calls = len(callTime)
            personDict[caller] += num_calls
            personDict[callee] += num_calls
    
    return sorted(tuple(zip(personDict.keys(), personDict.values())),key = lambda k: (-1 * k[1], k[0]))
    




