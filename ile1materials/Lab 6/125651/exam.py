def all_parties(db : {str:{str:[int]}}) -> {str}:
    #partiesdict = defaultdict(str)
    parties = set()
    for caller in db.keys():
        parties.add(caller)
        for receiver in db[caller].keys():
            parties.add(receiver)
            
    return parties



def read_db(file : open) -> {str:{str:[int]}}:
    callerdict = defaultdict(dict)
   # calleddict = defaultdict(list)
