def talker_frequency (db : {str:{str:[int]}}) -> [(str,int)]:
    callcountdict = defaultdict(int)
    for caller in db:
        for callee in db[caller]:
            #print(f'caller:{caller}\ncallee:{callee}\nlen:{len(db[caller][callee])}')
            callcountdict[caller] += len(db[caller][callee])
            callcountdict[callee] += len(db[caller][callee])
    #print(dict(callcountdict))
    return sorted([(caller, callcountdict[caller]) for caller in dict(callcountdict)], key=(lambda x : (-x[1],x[0])))



