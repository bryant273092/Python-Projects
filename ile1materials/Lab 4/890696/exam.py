def all_parties(db : {str:{str:[int]}}) -> {str}:
    party_set = set()
    
    for caller, receivers in db.items():
        for receiver, list_of_times in receivers.items():
            party_set.add(caller)
            party_set.add(receiver)
            
    return party_set
            



def read_db(file : open) -> {str:{str:[int]}}:
    db = defaultdict(dict)
    
    with file as f:
        for line in f:
            split_line = line.rstrip("\n").split(":")
            
            caller, receiver, list_of_times = split_line[0], split_line[1], split_line[2::]
            
            try:
                db[caller][receiver] += [int(time) for time in list_of_times]
            except KeyError:
                db[caller][receiver] = [int(time) for time in list_of_times]
            
    return dict(db)



def talker_frequency (db : {str:{str:[int]}}) -> [(str,int)]:
    frequency_dict = defaultdict(int)
    
    for caller, receivers in db.items():
        for receiver, list_of_times in receivers.items():
            frequency_dict[caller] += len(list_of_times)
            frequency_dict[receiver] += len(list_of_times)
            
    alphabetical = sorted(frequency_dict.items())
    final = sorted(alphabetical, key=lambda x: x[1], reverse=True)
    
    return final


def mutual_callers (db : {str:{str:[int]}}) -> {(str,str)}:
    mutuals = set()
    
    for caller, receivers in db.items():
        for receiver, list_of_times in receivers.items():
            if caller in db[receiver].keys():
                if (receiver, caller) not in mutuals and (caller, receiver) not in mutuals: # This might be redundant
                    if caller < receiver:
                        mutuals.add((caller, receiver))
                    elif receiver < caller:
                        mutuals.add((receiver, caller))
                
    return mutuals


def initiators (db : {str:{str:[int]}}) -> [str]:
    ilist = list()
    
    for caller, receivers in db.items():
        ilist.append(caller)
        
    
    receiver_times = dict()
    
    for caller, receivers in db.items():  # setup for first sort
        number_of_times_receiver = 0
        
        for c, rs in db.items():
            for r, lot in rs.items():
                if caller == r:
                    number_of_times_receiver += len(lot)
                    
        receiver_times[caller] = number_of_times_receiver
                
        
    ilist.sort(key=lambda c: receiver_times[c] , reverse=False) # increasing by number of times they are receiver
    ilist.sort(key=lambda c: sum([len(ts) for ts in db[c].values()]), reverse=True) # decreasing by number of times they are caller
        
    return ilist
            



def matrix (db : {str:{str:[int]}}) -> [[int]]:
    # create matrix
    
    number_of_people = len(db.keys())
    
    outer_array = list()
    
    for i in range(number_of_people):
        inner_array = []
        for j in range(number_of_people):
            inner_array.append(0)
        outer_array.append(inner_array)
        
    # iterate through db
    
    alphabetized_names = sorted(db.keys())
    
    for i in range(number_of_people):
        for j in range(number_of_people):
        
            if alphabetized_names[j] in db[alphabetized_names[i]].keys():
                for time in db[alphabetized_names[i]][alphabetized_names[j]]:
                    outer_array[i][j] += time
    
    return outer_array


