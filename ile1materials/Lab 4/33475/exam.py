def all_parties(db : {str:{str:[int]}}) -> {str}:
    results = set()
    for c,d in db.items():
        #Add all callers to the set
        results.add(c)
        for r,t in d.items():
            #Add all receivers and set will deal with the duplicates
            results.add(r)
    return results



def read_db(file : open) -> {str:{str:[int]}}:
    #Using lambda, we can make a really easy nested defaultdict :D
    results = defaultdict(lambda: defaultdict(list))
    #Iterate through all the lines then strip and split to get data
    for line in file:
        data = line.strip().split(":")
        #For every line, we know that the first and second values are the caller and receiever respectively
        #All we have to do is append the rest of the time data to the defaultdict at those keys
        for x in range(len(data)):
            if x > 1:
                results[data[0]][data[1]].append(int(data[x]))
    #Cast all inner dictionaries into regular dicts from defaultdicts
    for k,v in results.items():
        results[k] = dict(v)
    #Return the defaultdict as a dict so we have the correct data type
    return dict(results)



def talker_frequency (db : {str:{str:[int]}}) -> [(str,int)]:
    results = []
    #New defaultdict helps manage data
    data = defaultdict(int)
    #Iterate through original database
    for c,d in db.items():
        for r,t in d.items():
            for time in t:
                #Each time we find a call, we want to increment the call totals of both the caller and the receiever
                data[c] += 1
                data[r] += 1
    #Set up our list of tuples so we can return the correct data type
    for c,m in data.items():
        results.append((c,m))
    #Lastly, sort the list by decreasing order of calls, then by alphabetic order.
    #We cannot use reverse=True because we cannot apply a negative to a str value (as far as I'm aware at this moment)
    #So the easiest way to sort is by the negative call value to put it in decreasing order
    return sorted(results, key = lambda x: (-x[1], x[0]), reverse = False)



def mutual_callers (db : {str:{str:[int]}}) -> {(str,str)}:
    results = set()
    #New defaultdict helps manage data
    callDict = defaultdict(list)
    #Iterate through and append all the people that received calls from a caller to that caller's list
    for c,d in db.items():
        for r,t in d.items():
            callDict[c].append(r)
    #Iterate through the new dictionary and check for each caller and receiver if they are in each other's lists        
    for c,rList in callDict.items():
        for r in rList:
            if r in callDict.keys() and c in callDict[r]:
                #If they are both mutual callers, we want to add a nicely sorted tuple with them, so we use a list firsts
                entry = []
                entry.append(c)
                entry.append(r)
                results.add( tuple(sorted(entry)) )
    return results



def initiators (db : {str:{str:[int]}}) -> [str]:
    results = []
    #New defaultdict to help manage data
    data = defaultdict(list)
    #Iterate through database to set up new defaultdict with the template 
    #that records number of time initiated and number of times received
    for c,d in db.items():
        for r,t in d.items():
            data[c] = [0,0]
            data[r] = [0,0]
    #iterate through again
    for c,d in db.items():
        results.append(c)
        for r,t in d.items():
            for time in t:
                #Each call will increment one for the caller and one for the receiver in their respective slots
                data[c][0] += 1
                data[r][1] += 1
    #Finally, we sort first by the number of times initiated, then received in the opposite order, then by name
    return sorted(results, key=lambda x: (data[x][0], -data[x][1], data[x] )  , reverse=True)



def matrix (db : {str:{str:[int]}}) -> [[int]]:
    #Wow, this one was pretty hard!
    results = []
    #Have to keep track of what was changed since I kept ending up with doubled values in some slot
    changed = []
    newDict = defaultdict(lambda: defaultdict(int))
    #Set up new defaultdict by iterating through the database
    #This gives a defaultdict almost the same as the original, just instead of a list of calls with times
    #it contains a single sum time for that caller and receiver
    for c,d in db.items():
        for r,t in d.items():
            newDict[c][r] = 0
            for time in t:
                newDict[c][r] += time
    #Iterate through again to manage new data
    for c,d in db.items():
        for r,t in d.items():
            #Record the old value so that we can make sure each entry is properly changed
            oldval = newDict[c][r]
            #Add the two inverse slot partners together since the matrix doesn't care about time spent
            #calling or receiving, only total time spend on the phone
            newDict[c][r] += newDict[r][c]
            changed.append((c,r))
            #If we don't keep track of what's changed and edit it afterwards, we'll surely end up with doubled values
            if( (r,c) in changed):
                newDict[c][r] -= oldval
            #Also makes sure that each inverse slot is the same as its parter
            # i.e. for this kind of matrix, [1,2] is always the same as [2,1]
            if(newDict[r][c] == 0):
                newDict[r][c] = newDict[c][r]
    #Finally, we need to put the entire thing in alphabetical order
    names = sorted(newDict)
    #Construct the matrix with the values we already calculated
    #Lists appended to the main list for a 2d list
    for call in names:
        line = []
        for rec in names:
            line.append(newDict[call][rec])
        results.append(line)
    #And seemingly as if by black magic, the whole damn thing works!    
    return results




