def all_parties(db : {str:{str:[int]}}) -> {str}:
    returned_set = set()
    for caller,v in db.items():
        returned_set.add(caller)
        for receiver in v.keys():
            returned_set.add(receiver)
    return returned_set


def read_db(file : open) -> {str:{str:[int]}}:
    d = dict()
    for line in file:
        time_list = []
        temp = line.strip().split(":")
        caller = temp[0]
        receiver = temp[1]
        for length in temp[2:]:
            if caller not in d.keys():
                time_list.append(int(length))
                d.setdefault(caller, {receiver:[int(length)]})
            elif caller in d.keys():
                if receiver in d[caller].keys():
                    d[caller][receiver].append(int(length))
                else:
                    time_list.append(int(length))
                    d[caller].update({receiver:time_list})
               
    file.close()
    return d



def talker_frequency (db : {str:{str:[int]}}) -> [(str,int)]:
    d = dict()
    l = []
    for caller, v in db.items():
        for receiver,times in v.items():
            for time in times:
                if caller not in d.keys():
                    d.setdefault(caller, 1)
                elif caller in d.keys():
                    d[caller] += 1
                  
                if receiver not in d.keys():
                    d.setdefault(receiver, 1)
                elif receiver in d.keys():
                    d[receiver] += 1
    for person, amount in d.items():
        l.append((person,amount))
    return sorted(l, key = lambda x: (-x[1], x[0]))
                



