def all_parties(db : {str:{str:[int]}}) -> {str}:
    call_set = set()
    for c in db:
        for r in db[c]:
            call_set.add(r)
        call_set.add(c)
    return call_set



def read_db(file : open) -> {str:{str:[int]}}:
    caller_dict = dict()
    for line in file:
        caller_line = line.strip('\n').split(':')
        caller = caller_line[0]
        receiver = caller_line[1]
        minutes_call = caller_line[2:]
        if caller not in caller_dict:
            caller_dict.setdefault(caller, dict())
        if receiver not in caller_dict[caller]:
            caller_dict[caller].setdefault(receiver, list())
        for call in minutes_call:
            temp_list = caller_dict[caller].pop(receiver)
            temp_list.append(int(call))
            caller_dict[caller].setdefault(receiver, temp_list)
    return caller_dict



def talker_frequency (db : {str:{str:[int]}}) -> [(str,int)]:
    call_set = all_parties(db)
    frequency_list = list()
    for c in call_set:
        count = 0
        for caller in db:
            if c == caller:
                for r in db[caller]:
                    temp_list = db[caller].get(r)
                    for call in temp_list:
                        count += 1
            if c in db[caller]:
                temp_list = db[caller].get(c)
                for call in temp_list:
                    count += 1
        frequency_list.append((c, int(count)))
    return sorted(sorted(frequency_list, key = lambda x: x[0]), key = lambda x: x[1], reverse = True)



def mutual_callers (db : {str:{str:[int]}}) -> {(str,str)}:
    mutual_call_set = set()
    for c in db:
        for r in db[c]:
            if r in db and c in db[r]:
                if (r,c) not in mutual_call_set:
                    temp_list = sorted([c,r])
                    x = temp_list[0]
                    y = temp_list[1]
                    mutual_call_set.add((x,y))
    return mutual_call_set



def initiators (db : {str:{str:[int]}}) -> [str]:
    temp_list = list()
    parties = all_parties(db)
    for person in parties:
        called = 0
        received = 0
        if person in db:
            for c in db:
                if c == person:
                    for r in db[c]:
                        called += len(db[c].get(r))
                else:
                    if person in db[c]:
                        received += len(db[c].get(person))
        temp_list.append((person, called, received))
    temp_list = sorted(sorted(temp_list, key = lambda x: x[2]), key = lambda x: x[1], reverse = True)
    return [x[0] for x in temp_list]



