def all_parties(db : {str:{str:[int]}}) -> {str}:
    nameSet = set()
    for caller,receivers in db.items():
        nameSet.add(caller)
        for receiver in receivers:
            nameSet.add(receiver)
    return nameSet



def read_db(file : open) -> {str:{str:[int]}}:
    dataDict = defaultdict(dict)
    for line in file:
        line = line.rstrip("\n")
        tokens = line.split(":")
        dataDict[tokens[0]]
        numberList = []
        for i in range(2,len(tokens)):
            numberList.append(int(tokens[i]))
        if tokens[1] in dataDict[tokens[0]]:
            for item in numberList:
                dataDict[tokens[0]][tokens[1]].append(item)
        else:
            dataDict[tokens[0]][tokens[1]] = deepcopy(numberList)
        numberList.clear()
    return dict(dataDict)


def talker_frequency (db : {str:{str:[int]}}) -> [(str,int)]:
    frequencyDict = {}
    freqList = []
    for key,value in db.items():
        callerCount = 0
        receiverCount = 0
        for i in value:
            for num in db[key][i]:
                callerCount += 1
                receiverCount += 1
            if i in frequencyDict:
                frequencyDict[i] += receiverCount
            else:
                frequencyDict[i] = receiverCount
            receiverCount = 0
        if key in frequencyDict:
            frequencyDict[key] += callerCount
        else:
            frequencyDict[key] = callerCount
    for key,value in frequencyDict.items():
        tup = (key,value)
        freqList.append(tup)
    return sorted(freqList, key = lambda x: (-x[1],x[0]))
                
    



def mutual_callers (db : {str:{str:[int]}}) -> {(str,str)}:
    mutualSet = set()
    for key,value in db.items():
        for key2 in db.keys():
            if key2 in db[key] and (key,key2) not in mutualSet and key in db[key2]:
                mutualSet.add(tuple(sorted((key2,key))))
    return mutualSet



