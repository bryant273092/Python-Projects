def all_parties(db : {str:{str:[int]}}) -> {str}:
    parties = set()
    for caller in db:
        parties.add(caller)
        for receiver in db[caller]:
            parties.add(receiver)
    return parties



def read_db(file : open) -> {str:{str:[int]}}:
    calls = defaultdict(dict)
    for line in file:
        call = line.strip().split(":")
        if call[1] not in calls[call[0]].keys():
            calls[call[0]].update({call[1]:[int(call[2])]})
        else:
            calls[call[0]][call[1]].append(int(call[2]))
        if len(call) > 3:
            for length in range(3,len(call)):
                calls[call[0]][call[1]].append(int(call[length]))
    return dict(calls)



def talker_frequency (db : {str:{str:[int]}}) -> [(str,int)]:
    frequency = defaultdict(int)
    for caller in db:
        for receiver in db[caller]:
            for c in range(len(db[caller][receiver])):
                frequency[caller] += 1
                frequency[receiver] += 1
    return sorted(sorted([(x,y) for x,y in frequency.items()], key = lambda x: x[0]), key = lambda y:y[1], reverse = True)



def mutual_callers (db : {str:{str:[int]}}) -> {(str,str)}:
    mutual = set()
    for caller in db:
        for receiver in db[caller]:
            try:
                if db[receiver][caller] != None and (receiver,caller) not in mutual:
                    if caller < receiver:
                        mutual.add((caller,receiver))
                    else:
                        mutual.add((receiver,caller))
            except KeyError:
                pass
    return mutual



def initiators (db : {str:{str:[int]}}) -> [str]:
    callerNum = defaultdict(int)
    for caller in db:
        for receiver in db[caller]:
            for i in range(len(db[caller][receiver])):
                callerNum[caller] += 1 
    receiverNum = defaultdict(int)
    for caller in db:
        for receiver in db[caller]:
            for i in range(len(db[caller][receiver])):
                receiverNum[receiver] += 1
    callerList = [(x,y) for x,y in callerNum.items()]
    for caller in range(len(callerList)):
        callerList[caller] = (callerList[caller][0], callerList[caller][1], receiverNum[callerList[caller][0]])
    ordered = sorted(sorted(callerList, key = lambda x: x[2]), key = lambda y:y[1],reverse = True)
    result = []
    for e in ordered:
        result.append(e[0])
    return result


def matrix (db : {str:{str:[int]}}) -> [[int]]:
    callTime = defaultdict(int)
    peopleSet = set()
    for caller in db:
        peopleSet.add(caller)
        for receiver in db[caller]:
            peopleSet.add(receiver)
            for time in db[caller][receiver]:
                if (receiver,caller) in callTime.keys():
                    callTime[(receiver,caller)] += time
                else:
                    callTime[(caller,receiver)] += time
    callMatrix = []
    peopleList = sorted(list(peopleSet))
