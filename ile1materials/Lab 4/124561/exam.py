def all_parties(db : {str:{str:[int]}}) -> {str}:
    result = set()
    for c in db.keys():
        result.add(c)
        for r in db[c]:
            result.add(r)
    return result



def read_db(file : open) -> {str:{str:[int]}}:
    result = dict()
    for line in file:
        words = line.strip().split(':')
        c = words[0]
        r = words[1]
        t = [int(x) for x in words[2:]]
        if c in result:
            if r in result[c]:
                result[c][r]+=t
            else:
                result[c][r] = t
        else:
            result[c] = {r : t}
    return result



def talker_frequency (db : {str:{str:[int]}}) -> [(str,int)]:
    result = dict()
    for c in db.keys():
        if c not in result:
            result[c] = 0
        for r in db[c].keys():
            if r not in result:
                result[r] = 0
            result[c]+=len(db[c][r])
            result[r]+=len(db[c][r])
    return sorted(result.items(), key = lambda x: (-x[1],x[0]))



def mutual_callers (db : {str:{str:[int]}}) -> {(str,str)}:
    result = set()
    for c in db.keys():
        for r in db[c].keys():
            if c in db[r]:
                result.add(tuple(sorted([c, r])))
    return result



def initiators (db : {str:{str:[int]}}) -> [str]:
    result = dict()
    for c in db.keys():
        if c not in result:
            result[c] = [0,0]
        for r in db[c]:
            if r not in result:
                result[r] = [0,0]
            result[c][0] += len(db[c][r])
            result[r][1] += len(db[c][r]) 
    return sorted(result.keys(), key = lambda x: (-result[x][0], result[x][1],x))



def matrix (db : {str:{str:[int]}}) -> [[int]]:
    names = sorted(db.keys())
    times = len(db.keys())
    result = [[0 for i in range(times)] for i in range(times)]
    for i in range(times):
        for j in range(times):
            if names[j] in db[names[i]]:
                new = sum(db[names[i]][names[j]])
                result[i][j] += new
                result[j][i] += new
    return result
            





