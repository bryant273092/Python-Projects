def all_parties(db : {str:{str:[int]}}) -> {str}:
    final = set()
    for caller in db:
        final.add(caller)
        for rec in db[caller]:
            final.add(rec)
            
    return final


def read_db(file : open) -> {str:{str:[int]}}:
    
    d = defaultdict(dict)
    
    for line in file:
        con = line.strip().split(':')
        if con[1] not in d[con[0]]:
            d[con[0]][con[1]] = []
        for num in con[2:]:
            d[con[0]][con[1]].append(int(num))
            
    
    return dict(d)



def talker_frequency (db : {str:{str:[int]}}) -> [(str,int)]:
    
    final = []
    for caller in db:
        counter = 0
        for rec in db[caller]:
            counter += len(db[caller][rec])
        for check in db:
            if caller in db[check]:
                counter += len(db[check][caller])
        
        final.append((caller, counter))
        
    return sorted(final, key = lambda t: (-t[1], t[0]))



def mutual_callers (db : {str:{str:[int]}}) -> {(str,str)}:
    
    final = set()
    
    for caller in db:
        for rec in db[caller]:
            if len(db[caller][rec]) > 0:
                if rec in db:
                    if caller in db[rec]:
                        if len(db[rec][caller]) > 0:
                            l = []
                            l.append(caller)
                            l.append(rec)
                            final.add(tuple(sorted(l)))

    return final

def initiators (db : {str:{str:[int]}}) -> [str]:
    
    final = []
    for caller in db:
        caller_calls = 0
        rec_calls = 0
        for rec in db[caller]:
            caller_calls += len(db[caller][rec])
        
        for check in db:
            if caller in db[check]:
                rec_calls += len(db[check][caller])
        final.append((caller, caller_calls, rec_calls))
    
    final = sorted(final, key = lambda t: (-t[1], t[2], t[0]))
    fin = []
    for tup in final:
        fin.append(tup[0])
    
    return fin
            


