def all_parties(db : {str:{str:[int]}}) -> {str}:
    aset = set()
    for i in db:
        aset.add(i)
        for name in db[i]:
            aset.add(name)
    return aset



def read_db(file : open) -> {str:{str:[int]}}:
    adict = defaultdict(dict)
    for line in file:
        caller, receiver, call_list= line.split(':')[0], line.split(':')[1], line.split(':')[2:]
        call_list = [int(i.strip('\n')) for i in call_list]
        if caller not in adict:
            adict[caller] = {}
        if receiver not in adict[caller]:
            adict[caller][receiver] = call_list
        elif receiver in adict[caller]:
            adict[caller][receiver] += (i for i in call_list)
    return adict
        



def talker_frequency (db : {str:{str:[int]}}) -> [(str,int)]:
    alist = []
    for i in db:
        count = 0
        for j in db:
            for k in db[j]:
                if k == i:
                    count += len(db[j][k])
        for n in db[i]:
            count += len(db[i][n])
        alist.append((i, count))
        
    return sorted(sorted(alist), key = lambda x: -x[1])
        
                



def mutual_callers (db : {str:{str:[int]}}) -> {(str,str)}:
    aset = set()
    for i in db:
        receivers = [j for j in db[i]]
        for n in receivers:
            for k in db[n]:
                if i == k:
                    alist = [i,n]
                    alist.sort()
                    aset.add(tuple(i for i in alist))
    return aset
                



def initiators (db : {str:{str:[int]}}) -> [str]:
    adict = defaultdict(list)
    for i in db:
        adict[i] = [0,0]
        for n in db[i]:
            adict[i][0] += len(db[i][n])
        for j in db:
            for k in db[j]:
                if k == i:
                    adict[i][1] += len(db[j][k])
    return sorted(sorted(sorted(adict), key = lambda x: adict[x][1]), key = lambda x: -adict[x][0])



