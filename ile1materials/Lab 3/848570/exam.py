def all_parties(db : {str:{str:[int]}}) -> {str}:
    db_set = set()
    for caller in db:
        for receiver in db[caller]:
            db_set.add(receiver)
    return db_set



def read_db(file : open) -> {str:{str:[int]}}:
    db = {}
    for line in file:
        sp = line.split(':')
        caller = sp[0]
        receiver = sp[1]
        calls = sp[2:]
        for call in calls:
            db.setdefault(caller,{})
            db[caller].setdefault(receiver,[])
            db[caller][receiver].append(int(call.rstrip('\n')))
    return db



def talker_frequency (db : {str:{str:[int]}}) -> [(str,int)]:
    '''
    freq_dict is in the form:
    {
    "name" : #ofcalls
    }
    '''
    freq_dict = dict()
    for caller in db:
        freq_dict.setdefault(caller,0)
        for receiver in db[caller]:
            freq_dict.setdefault(receiver,0)
            freq_dict[receiver] += len(db[caller][receiver])
            freq_dict[caller] += len(db[caller][receiver])
    '''
    DID IT IN ONE LINE LETS FUCKIN GOOOO
    '''
    return [(name,freq_dict[name]) for name in sorted(sorted(freq_dict),key=lambda x:freq_dict[x],reverse=True)]
    



def mutual_callers (db : {str:{str:[int]}}) -> {(str,str)}:
    '''
    mutual_dict is in the form
    {
    "name" : {"set of names"}
    }
    '''
    mutual_dict = dict()
    for caller in db:
        mutual_dict.setdefault(caller,set())
        for receiver in db[caller]:
            mutual_dict.setdefault(receiver,set())
            mutual_dict[caller].add(receiver)
    mutual_set = set()
    for name in mutual_dict:
        for receiver in mutual_dict[name]:
            if name in mutual_dict[receiver] and receiver in mutual_dict[name]:
                mutual_set.add(tuple(sorted([name,receiver])))
    return mutual_set


def initiators (db : {str:{str:[int]}}) -> [str]:
    '''
    init_dict is in the form
    {
    "name": (# of caller, # of receiver)
    }
    '''
    init_dict = dict()
    for caller in db:
        init_dict.setdefault(caller, [0,0])
        for receiver in db[caller]:
            init_dict.setdefault(receiver,[0,0])
            init_dict[caller][0] += len(db[caller][receiver])
            init_dict[receiver][1] += len(db[caller][receiver])
    '''
    RETURN IT IN ONE LINE GET REKT
    '''
    return [name for name in sorted(sorted(init_dict,key=lambda x:init_dict[x][1]),key=lambda x:init_dict[x][0],reverse=True)]



