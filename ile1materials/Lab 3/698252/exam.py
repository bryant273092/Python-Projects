def all_parties(db : {str:{str:[int]}}) -> {str}:
    set_names = set()
    for caller,cval in db.items():
        if caller not in set_names:
            set_names.add(caller)
        for receiver in cval.keys():
            if receiver not in set_names:
                set_names.add(receiver)
    return set_names



