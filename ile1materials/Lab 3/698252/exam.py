def talker_frequency (db : {str:{str:[int]}}) -> [(str,int)]:
    list_names = list()
    list_used = list()
    for caller,cval in db.items():
        for receiver,minutes in cval.items():
            if caller not in list_used:
                list_names.append((caller,len(minutes)))
            else:
                for x,y in list_names:
                    if x==caller:
                        newval = y + len(minutes)
                        del list_names[list_names.index((x,y))]
                        list_names.append((caller,newval))
                        break
            if receiver not in list_used:
                list_names.append((receiver,len(minutes)))
            else:
                for x,y in list_names:
                    if x==receiver:
                        newval = y + len(minutes)
                        del list_names[list_names.index((x,y))]
                        list_names.append((receiver,newval))
                        break
            if caller not in list_used:
                list_used.append(caller)
            if receiver not in list_used:
                list_used.append(receiver)
    return sorted(((x,y) for x,y in list_names), key= lambda x: (-x[1],x[0]))
            



def mutual_callers (db : {str:{str:[int]}}) -> {(str,str)}:
    mutual = set()
    caller_list = []
    rec_list = []
    for caller, cval in db.items():
        for receiver, minutes in cval.items():
            if (caller,receiver) in caller_list or (receiver, caller) in caller_list:
                mutual.add(tuple(sorted((caller,receiver))))
            else:
                caller_list.append((caller,receiver))
     
    return mutual



