def all_parties(db : {str:{str:[int]}}) -> {str}:
    result = set()
    for c in db:
        result.add(c)
        for r in db[c]:
            result.add(r)
    return result



def read_db(file : open) -> {str:{str:[int]}}:
    result = {}
    for lines in file.readlines():
        lines = lines.rstrip('\n')
        phone = lines.split(':')
        times = []
        x = 2
        while True:
            try:
                times.append(int(phone[x]))
                x = x+1
            except IndexError:
                break
        if phone[0] not in result:
            result[phone[0]] = {phone[1]:times}
        else:
            if phone[1] in result[phone[0]]:
                result[phone[0]][phone[1]].extend(times)
            else:
                result[phone[0]].update({phone[1]:times})
    return result
        



def talker_frequency (db : {str:{str:[int]}}) -> [(str,int)]:
    result_p = []
    result_t = []
    for c in db:
        total_len = 0
        for r in db[c]:
            if r in result_p:
                pos = result_p.index(r)
                result_t[pos] = result_t[pos] + len(db[c][r])
            else:
                result_p.append(r)
                result_t.append(len(db[c][r]))
            total_len += len(db[c][r])
        if c not in result_p:  
            result_p.append(c)
            result_t.append(total_len)
        else:
            pos = result_p.index(c)
            result_t[pos] = result_t[pos] + total_len
    return sorted(sorted(list(zip(result_p,result_t))),key = lambda item:item[1],reverse = True)


def mutual_callers (db : {str:{str:[int]}}) -> {(str,str)}:
    sub_result = set()
    result = set()
    for caller in db:
        for call in db:
            if caller in db[call] and call in db[caller]:
                if (call,caller) in sub_result:
                    pass
                else:
                    sub_result.add((caller,call))
    for names in sub_result:
        if names[0] < names[1]:
            result.add(names)
        else:
            result.add((names[1],names[0]))
    return result


def initiators (db : {str:{str:[int]}}) -> [str]:
    person_c = []
    person_r = []
    call = []
    rece = []
    for c in db:
        person_c.append(c)
        total = 0
        for r in db[c]:
            if r not in person_r:
                person_r.append(r)
                rece.append(len(db[c][r]))
            else:
                pos = person_r.index(r)
                rece[pos] = rece[pos] + len(db[c][r])
            total += len(db[c][r])
        call.append(total)
    caller = sorted(list(zip(person_c,call)))
    receiver = sorted(list(zip(person_r,rece)))
    result = list(zip(caller,receiver))
    lists = sorted(sorted(result,key = lambda item:item[1][1]),key = lambda item:item[0][1],reverse = True)
    final = []
    for a in lists:
        final.append(a[0][0])
    return final
            
            



