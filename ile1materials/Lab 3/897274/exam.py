def all_parties(db : {str:{str:[int]}}) -> {str}:
    result = set()
    for caller in db:
        result.add(caller)
        
        for rec in db[caller]:
            result.add(rec)
    return result
            



def read_db(file : open) -> {str:{str:[int]}}:
    result = defaultdict(dict)
    
    for line in file:
        caller = line.split(':')[0]
        rec = line.rstrip().split(':')[1:]
        
        if caller not in result:
            result[caller].update({rec[0]:list_to_int(rec[1:])})
        elif rec[0] not in result[caller]:
            result[caller].update({rec[0]:list_to_int(rec[1:])})
        else:
            result[caller][rec[0]].extend(list_to_int(rec[1:]))
    
    return result
        
def list_to_int(L:[list]):
    for i in range(len(L)):
        L[i] = int(L[i])
    return L



