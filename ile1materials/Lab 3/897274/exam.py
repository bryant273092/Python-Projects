def mutual_callers (db : {str:{str:[int]}}) -> {(str,str)}:
    result = set()
    
    process = dict()
    
    for k in db:
        process.update({k:list(db[k].keys())})
    
    for caller,recs in process.items():
        for rec in recs:
            if rec in process and caller in process[rec]:
                item = sorted((rec,caller))
                result.add(tuple(item))
    return result
        

