def all_parties(db : {str:{str:[int]}}) -> {str}:
    result = set()
    for k,v in db.items():
        result.add(k)
        for person in v:
            result.add(person)
    return result



def read_db(file : open) -> {str:{str:[int]}}:
    result = defaultdict(dict)
    
    for line in file:
        line = line.strip().split(':')
        c, r = line[0], line[1]
        for calls in line[2:]:
            if r in result[c]:
                result[c][r].append(int(calls))
            else:
                result[c][r] = [int(calls)]
                
    return dict(result)



def talker_frequency (db : {str:{str:[int]}}) -> [(str,int)]:
    d = defaultdict(int)
    for k,v in db.items():
        for r,call in v.items():
            d[k] += len(call)
            d[r] += len(call)
    return sorted([(k,int(v)) for k,v in d.items()], key = lambda x: (-x[1], x[0]))
        



def mutual_callers (db : {str:{str:[int]}}) -> {(str,str)}:
    result = set()
    for k,v in db.items():
        for r in v:
            if r in db:
                if k in db[r]:
                    result.add((k,r) if k<r else (r,k))
    return result
                



def initiators (db : {str:{str:[int]}}) -> [str]:
    d = defaultdict(list)
    for k,v in db.items():
        for r,call in v.items():
            if d[k]: d[k][0] += len(call)
            else: d[k] = [len(call), 0]
            
            if d[r]: d[r][1] += len(call)
            else: d[r] = [0, len(call)]

    return sorted([k for k,v in d.items()], key = lambda x: (-d[x][0], d[x][1], x))




