def all_parties(db : {str:{str:[int]}}) -> {str}:
    result = set()
    for caller in db.keys():
        for reciever in db[caller].keys():
            result.add(caller)
            result.add(reciever)
    return result



def read_db(file : open) -> {str:{str:[int]}}:
    result = defaultdict(dict)
    for line in file:
        data = line.rstrip('\n').split(':')
        caller = data[0]
        reciever = data[1]
        calls = [int(time) for time in data[2:]]
        result[caller][reciever] = calls
    return result



