def talker_frequency (db : {str:{str:[int]}}) -> [(str,int)]:
    temp = []
    for caller in db.keys():
        other_callers = db.copy()
        calls_made = 0
        calls_recieved = 0
        for calls in db[caller].values():
            calls_made += len(calls)
        del other_callers[caller]
        for other_caller in other_callers:
            if caller in other_callers[other_caller].keys():
                calls_recieved += len(other_callers[other_caller][caller])
        temp.append((caller,calls_made+calls_recieved))
        result = sorted(temp,key=lambda t: (-t[1],t[0]))
    return result
                


def mutual_callers (db : {str:{str:[int]}}) -> {(str,str)}:
    result = set()
    for caller in db.keys():
        who_they_called = list(db[caller].keys())
        for reciever in who_they_called:
            if caller in list(db[reciever].keys()):
                couple = [caller,reciever]
                sortedcouple = sorted(couple)
                result.add(tuple(sortedcouple))
    return result



