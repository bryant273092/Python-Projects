def all_parties(db : {str:{str:[int]}}) -> {str}:
    all = set()
    for caller in db:
        for receiver in db[caller]:
            all.add(caller)
            all.add(receiver)

    return all



def read_db(file : open) -> {str:{str:[int]}}:
    all = dict()
    for line in file:
        formatted = line.strip().split(':')
        caller = formatted[0]
        receiver = formatted[1] 
        times = formatted[2:]

        if caller not in all:            
            all_rec = dict()
            all[caller] = all_rec
        
        
        if receiver in all_rec:
            for time in times:
                all_rec[receiver].append(int(time))
        else:
            all_rec[receiver] = times
        for x in range(len(times)):
            times[x] = int(times[x])
    
    return all
