def talker_frequency (db : {str:{str:[int]}}) -> [(str,int)]:
    final = []
    temp = dict()
    for caller in db:
        for receiver in db[caller].items():
            if caller not in temp:
                temp[caller] = 0
            how_much_went = 0
            for num in receiver[1]:      #[1] is the list  [0] is the receiver
                temp[caller] += 1
                how_much_went += 1
            if receiver[0] not in temp:
                temp[receiver[0]] = 0
            temp[receiver[0]] += how_much_went
    for person in temp:
        final.append((person, temp[person]))
    final = [x for x in sorted(final, key = (lambda item: (-item[1], item[0])))]
            
    return final


