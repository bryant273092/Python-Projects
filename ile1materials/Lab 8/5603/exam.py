def mutual_callers (db : {str:{str:[int]}}) -> {(str,str)}:
    real_final = set()
    final = []
    temp = []
    for caller in db:
        for receiver in db[caller].items():
            temp.append([caller, receiver[0]])
    for caller in db:
        for receiver in db[caller].items():
            if [caller, receiver[0]] and [receiver[0], caller] in temp:
                if (caller, receiver[0]) or (receiver[0], caller) not in final: 
                    final.append([caller, receiver[0]])
    for pair in final:
        pair.sort()
    for pair in final:
        real_final.add(tuple(pair))


    return real_final

