def all_parties(db : {str:{str:[int]}}) -> {str}:
    set_of_names = set()
    
    for caller in db:
        set_of_names.add(caller)
        for receiver in db[caller]:
            set_of_names.add(receiver)
            

    return set_of_names



def read_db(file : open) -> {str:{str:[int]}}:
    the_db = {}
    
    the_lines = [] 
     
    for line in file:
        the_lines.append(line)
        line = line.rstrip()
        split_line = line.split(':')
        caller = split_line[0]
        caller = caller.rstrip()
        
        receiver = split_line[1]
        receiver = receiver.rstrip()
        times = []
        for i in range(2, len(split_line)):
            times.append(int(split_line[i]))
        
        
        
        
        
        
        if the_db.get(caller) is None:
            the_db[caller] = {}
            the_db[caller][receiver] = times
        else:
            if the_db[caller].get(receiver) is None:
                the_db[caller][receiver] = times
            else:
                for time in times:
                    the_db[caller][receiver].append(time)
        
    
    return the_db



