from collections import defaultdict   # If you choose to use defaultdict  

# You might use these abbreviations in your code
# c  : caller
# r  : receiver
# t  : time
# ts : list of times


def all_parties(db : {str:{str:[int]}}) -> {str}:
    result = set()
    for c in db:
        result.add(c)
        for r in db[c]:
            result.add(r)
    return result


def read_db(file : open) -> {str:{str:[int]}}:
    result = defaultdict(defaultdict)
    for line in file:
        line = line.rstrip().split(':')
        c, r, ts = line[0], line[1], line[2:]
        if r not in result[c]: result[c][r] = [int(t) for t in ts]
        else: result[c][r].extend(int(t) for t in ts)
    return result
        

def talker_frequency (db : {str:{str:[int]}}) -> [(str,int)]:
    result = defaultdict(int)
    for c in db:
        for r in db[c]:
            result[c] += len(db[c][r])
            result[r] += len(db[c][r])
    return sorted(sorted(((c,result[c]) for c in result)), key = (lambda x: x[1]), reverse = True)


def mutual_callers (db : {str:{str:[int]}}) -> {(str,str)}:
    result = defaultdict(int)
    for c in db:
        for r in db[c]:
            result[tuple(sorted([c,r]))] += 1
    dict_copy = result.copy()
    for pair in result:
        if result[pair] == 1: del dict_copy[pair]
    return set(tuple(pair) for pair in dict_copy)


'''
{name: {c: int, r: int}}
{name: [c,r]}
'''
def initiators (db : {str:{str:[int]}}) -> [str]:
    temp = defaultdict(list)
    for c in db:
        for r in db[c]:
            if c not in temp.keys(): temp[c] = [len(db[c][r]), 0]
            else: temp[c][0] += len(db[c][r])
            if r not in temp.keys(): temp[r] =  [0, len(db[c][r])]
            else: temp[r][1] += len(db[c][r])
    return sorted(sorted(sorted((name for name in temp), key = (lambda x: x)), key = (lambda x: temp[x][1])), key = (lambda x: temp[x][0]), reverse = True)


