def matrix (db : {str:{str:[int]}}) -> [[int]]:
    talkers = sorted(list(all_parties(db)))
    master = list()
    for x in talkers:
        single = list()
        for y in talkers:
            if db[x].get(y) == None:
                single.append(0)
            else: 
                z = 0
                for t in db[x].get(y):   
                    z += t
                
                single.append(z)
        
        master.append(single)    
    
    return master
            
            
        





