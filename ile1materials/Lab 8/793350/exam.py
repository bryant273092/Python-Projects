def all_parties(db : {str:{str:[int]}}) -> {str}:
    callers = list()
    for k, v in db.items():
        callers.append(k)
        for recipient in v.keys():
            callers.append(recipient)
            
    return set(callers)



def read_db(file : open) -> {str:{str:[int]}}:
    call_dict = defaultdict(dict)
    for line in file.readlines():
        x = line.strip('\n').split(':')
        if call_dict[x[0]].get(x[1]) != None:
            for call in [int(time) for time in x[2:]]:
                call_dict[x[0]][x[1]].append(int(call))
        else:
            call_dict[x[0]].update({x[1]: [int(time) for time in x[2:]]})
        
    return dict(call_dict)
        
    
    



def talker_frequency (db : {str:{str:[int]}}) -> [(str,int)]:
    talkers = list(all_parties(db))
    freq_dict = dict()
    for person in talkers:
        freq_dict[person] = 0
    for k, v in db.items():
        for person in freq_dict.keys():
            if k == person:
                for x, y in v.items():
                    for i in range(len(y)):
                        freq_dict[person] += 1
    for v in db.values():
        for k, v in v.items():
            for person in freq_dict.keys():
                if k == person:
                    for i in range(len(v)):
                        freq_dict[person] += 1
        
    return [(k, int(v)) for k, v in sorted(freq_dict.items(), key=lambda x : (-x[1], x[0]))]
        



def mutual_callers (db : {str:{str:[int]}}) -> {(str,str)}:
    mutual = set()
    for k, v in db.items():
        for reciever in v.keys():
            if db[reciever].get(k) != None:
                if (reciever, k) not in mutual:
                    mutual.add(tuple(x for x in sorted([k, reciever])))
    
    return mutual           
               


